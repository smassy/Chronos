namespace App\Controller;

use Cake\Controller\Controller;
use Cake\Event\Event;

// Constants for controllers
// Facilitate role handling.
const INACTIVE = 0;
const USER = 20;
const SECRETARY = 40;
const MANAGER = 120;
const ADMIN = 220;
const SUPERADMIN = 255;
// Default route home
const HOME = ['controller' => 'Users', 'action' => 'index'];

/**
 * Application Controller
 *
 * Add your application-wide methods in the class below, your controllers
 * will inherit them.
 *
 * @link https://book.cakephp.org/3.0/en/controllers.html#the-app-controller
 */
class AppController extends Controller
{

    /**
     * Initialization hook method.
     *
     * Use this method to add common initialization code like loading components.
     *
     * e.g. `$this->loadComponent('Security');`
     *
     * @return void
     */
    public function initialize()
    {
        parent::initialize();

        $this->loadComponent('RequestHandler', [
            'enableBeforeRedirect' => false,
        ]);
        $this->loadComponent('Flash');
        $this->loadComponent('Auth', [
            'authenticate' => [
                'Form' => [
                    'fields' => [
                        'username' => 'username',
                        'password' => 'password'
                    ]
                ]
            ],
            'loginAction' => [
                'controller' => 'Users',
                'action' => 'login'
            ],
            'authorize' => ['Controller'],
            "unauthorizedRedirect" => ["controller" => "Users", "action" => "login"]
        ]);

        /*
         * Enable the following component for recommended CakePHP security settings.
         * see https://book.cakephp.org/3.0/en/controllers/components/security.html
         */
        //$this->loadComponent('Security');
    }

    // Intended to be overridden in individual controllers
    public function isAuthorized($user) {
        return true;
    }
}
